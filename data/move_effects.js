window.describeBattleEffect = window.describeBattleEffect || function (effectId) {
  var numericId = Number(effectId);
  if (!Number.isFinite(numericId) || numericId < 0) return "UnknownEffect_" + String(effectId);
  if (numericId < window.DDEX_MOVE_EFFECT_DESCRIPTIONS.length) return window.DDEX_MOVE_EFFECT_DESCRIPTIONS[numericId];
  return "UnknownEffect_" + String(numericId);
};
